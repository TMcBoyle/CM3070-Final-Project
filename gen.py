""" Bitboard generating helper file """

n = int(    
          "".join("00000000"[::-1])
        + "".join("00000000"[::-1])
        + "".join("00000000"[::-1])
        + "".join("00000000"[::-1])
        + "".join("00000000"[::-1])
        + "".join("00000000"[::-1])
        + "".join("11111111"[::-1])
        + "".join("00000000"[::-1])
    , base=2)

print(f"0x{n:016x}")
